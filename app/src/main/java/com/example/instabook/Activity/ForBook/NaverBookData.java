package com.example.instabook.Activity.ForBook;

public class NaverBookData {
    String title;
    String author;
    String pub;
    String isbn;
    String imag;

    public NaverBookData(){

    }

    public void setNtitle(String title) { this.title = title; }

    public String getNtitle() { return title; }

    public void setNauthor(String author) { this.author = author; }

    public String getNauthor() { return author; }

    public void setNpub(String pub) { this.pub = pub; }

    public String getNpub() { return pub; }

    public void setNisbn(String isbn) { this.isbn = isbn; }

    public String getNisbn() { return isbn; }

    public void setNimag(String imag) { this.imag = imag; }

    public String getNimag() { return imag; }
}
