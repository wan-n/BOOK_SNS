package com.example.instabook.ListView;

public class UBookListItem {
    String BookName;
    String Publisher;
    String BookImageUrl;
    String ISBN13;
    int UserBookUID;

    public String getPublisher() {
        return Publisher;
    }

    public int getUserBookUID() {
        return UserBookUID;
    }

    public String getBookName() {
        return BookName;
    }

    public String getISBN13() {
        return ISBN13;
    }

    public String getBookImageUrl() {
        return BookImageUrl;
    }
}
